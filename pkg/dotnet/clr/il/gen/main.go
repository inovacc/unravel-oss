// Command gen parses CoreCLR opcode.def into opcodes.go. Run: go run ./gen.
package main

import (
	"bufio"
	"bytes"
	_ "embed"
	"fmt"
	"os"
	"path/filepath"
	"regexp"
	"runtime"
	"sort"
	"strings"
)

// opcodeDef is the vendored CoreCLR opcode.def, embedded at compile time so the
// generator finds it regardless of the working directory go generate runs in.
//
//go:embed opcode.def
var opcodeDef []byte

// operandMap maps opcode.def InlineXxx names to our OperandClass constant names.
// Phantom classes (e.g. InlineNone2) are intentionally absent → a def row using
// one fails the build, which is the guard the spec wants.
var operandMap = map[string]string{
	"InlineNone": "InlineNone", "InlineI": "InlineI", "ShortInlineI": "ShortInlineI",
	"InlineI8": "InlineI8", "InlineR": "InlineR", "ShortInlineR": "ShortInlineR",
	"InlineVar": "InlineVar", "ShortInlineVar": "ShortInlineVar",
	"InlineBrTarget": "InlineBrTarget", "ShortInlineBrTarget": "ShortInlineBrTarget",
	"InlineSwitch": "InlineSwitch", "InlineMethod": "InlineMethod", "InlineField": "InlineField",
	"InlineType": "InlineType", "InlineTok": "InlineTok", "InlineString": "InlineString",
	"InlineSig": "InlineSig", "InlinePhi": "", // InlinePhi is unused in practice → skip.
}

var opdef = regexp.MustCompile(`OPDEF\(\s*\w+\s*,\s*"([^"]+)"\s*,[^,]+,[^,]+,\s*(\w+)\s*,[^,]+,\s*(\d+)\s*,\s*(0x[0-9A-Fa-f]+)\s*,\s*(0x[0-9A-Fa-f]+)`)

type row struct {
	name     string
	operand  string
	prefixed bool
	code     byte
}

func main() {
	var rows []row
	sc := bufio.NewScanner(bytes.NewReader(opcodeDef))
	for sc.Scan() {
		m := opdef.FindStringSubmatch(sc.Text())
		if m == nil {
			continue
		}
		name, operandDef := m[1], m[2]
		if name == "unused" || strings.HasPrefix(name, "prefix") {
			continue
		}
		cls, ok := operandMap[operandDef]
		if !ok {
			fmt.Fprintf(os.Stderr, "unknown operand class %q for opcode %q\n", operandDef, name)
			os.Exit(1)
		}
		if cls == "" {
			continue // InlinePhi etc.
		}
		var b1, b2 uint64
		fmt.Sscanf(m[4], "0x%x", &b1)
		fmt.Sscanf(m[5], "0x%x", &b2)
		rows = append(rows, row{name: name, operand: cls, prefixed: b1 == 0xFE, code: byte(b2)})
	}
	if err := sc.Err(); err != nil {
		fmt.Fprintln(os.Stderr, "scan:", err)
		os.Exit(1)
	}
	sort.Slice(rows, func(i, j int) bool {
		if rows[i].prefixed != rows[j].prefixed {
			return !rows[i].prefixed
		}
		return rows[i].code < rows[j].code
	})

	var b strings.Builder
	b.WriteString("/*\nCopyright (c) 2026 Security Research\n*/\n\n")
	b.WriteString("// Code generated by ./gen from CoreCLR opcode.def; DO NOT EDIT.\n\n")
	b.WriteString("package il\n\nvar opcodeTable = []Opcode{\n")
	for _, r := range rows {
		fmt.Fprintf(&b, "\t{Name: %q, Code: %#02x, Prefixed: %t, Operand: %s},\n", r.name, r.code, r.prefixed, r.operand)
	}
	b.WriteString("}\n")
	if err := os.WriteFile(outputPath(), []byte(b.String()), 0o644); err != nil {
		fmt.Fprintln(os.Stderr, "write opcodes.go:", err)
		os.Exit(1)
	}
}

// outputPath returns the absolute path of the generated opcodes.go, computed
// from this generator's own source location so it is correct regardless of the
// working directory go generate runs in. The output lives in the parent il
// package (one level up from this gen command's directory).
func outputPath() string {
	_, self, _, ok := runtime.Caller(0)
	if !ok {
		fmt.Fprintln(os.Stderr, "cannot resolve generator source path")
		os.Exit(1)
	}
	return filepath.Join(filepath.Dir(self), "..", "opcodes.go")
}
